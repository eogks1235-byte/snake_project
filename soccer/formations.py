def matchup_mult(attacker_form: str, defender_form: str) -> float:
    """없는 조합은 1.0 (대칭/근사). 새 포메이션(3-4-3, 4-2-4)도 자연스럽게."""
    if (attacker_form, defender_form) in FORMATION_MATCHUP:
        return FORMATION_MATCHUP[(attacker_form, defender_form)]
    # 3-4-3: 4-3-3보다 약간 더 공격적 → 비슷한 효과 + 0.02 보정
    if attacker_form == '3-4-3':
        base = FORMATION_MATCHUP.get(('4-3-3', defender_form), 1.0) + 0.02
        return base
    if defender_form == '3-4-3':
        # 3-4-3는 백3라 공격에 취약 — 공격팀 우위 +0.04
        base = FORMATION_MATCHUP.get((attacker_form, '4-3-3'), 1.0) + 0.04
        return base
    # 4-2-4: 극단적 공격 → 4-3-3보다 +0.05 공격, 단 수비 약점 ↑
    if attacker_form == '4-2-4':
        base = FORMATION_MATCHUP.get(('4-3-3', defender_form), 1.0) + 0.05
        return base
    if defender_form == '4-2-4':
        # 4-2-4는 백4지만 미드 약함 → 공격팀 큰 우위 +0.06
        base = FORMATION_MATCHUP.get((attacker_form, '4-3-3'), 1.0) + 0.06
        return base
    return 1.0


