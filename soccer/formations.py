def positions_away(formation: str) -> list:
    """어웨이(우→좌 공격)는 fx 미러링."""
    base = FORMATIONS.get(formation, FORMATIONS['4-2-3-1'])
    return [(role, 1.0 - fx, fy) for role, fx, fy in base]


