# ────────────────────────────────────────────
#  constants.py
#  게임 전반에서 공통으로 사용하는 상수 모음
#  여기서 값을 바꾸면 게임 전체에 자동 반영됨
# ────────────────────────────────────────────

# ── 화면 크기 (픽셀) ─────────────────────────
WINDOW_WIDTH  = 480   # 가로 480px
WINDOW_HEIGHT = 480   # 세로 480px

# ── 격자 한 칸의 크기 ────────────────────────
# 20px짜리 칸으로 나누면 480 / 20 = 24칸
CELL_SIZE = 20

# ── 격자 칸 수 ──────────────────────────────
# 뱀과 먹이의 좌표는 픽셀이 아닌 격자 단위로 관리됨
GRID_W = WINDOW_WIDTH  // CELL_SIZE   # 가로 24칸
GRID_H = WINDOW_HEIGHT // CELL_SIZE   # 세로 24칸

# ── FPS (초당 프레임 수) ─────────────────────
# 높을수록 빠르게 움직임. 학습 시엔 render_mode="none"이라 FPS 무관
FPS_HUMAN = 15   # 사람이 직접 플레이할 때 속도 (느려야 키 조작 가능)
FPS_WATCH = 30   # AI 플레이를 관람할 때 속도 (빠르게 봐도 OK)

# ── 색상 (R, G, B) ───────────────────────────
BLACK  = (  0,   0,   0)   # 배경
WHITE  = (255, 255, 255)   # 텍스트
GREEN  = ( 34, 177,  76)   # 뱀 머리
DGREEN = ( 20, 120,  50)   # 뱀 몸통 (머리보다 어둡게)
RED    = (220,  50,  50)   # 먹이
GRAY   = ( 40,  40,  40)   # 격자 선

# ── 방향 상수 (dx, dy) ───────────────────────
# pygame 좌표계: 오른쪽이 +x, 아래쪽이 +y
UP    = ( 0, -1)   # 위 = y 감소
DOWN  = ( 0,  1)   # 아래 = y 증가
LEFT  = (-1,  0)   # 왼쪽 = x 감소
RIGHT = ( 1,  0)   # 오른쪽 = x 증가

# 시계 방향 순서로 정렬: 인덱스 +1 = 오른쪽 회전, -1 = 왼쪽 회전
# snake_game.py의 _turn()과 snake_env.py의 _get_state()에서 활용
DIRECTIONS = [UP, RIGHT, DOWN, LEFT]
